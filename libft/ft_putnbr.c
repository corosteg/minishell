#include "libft.h"

void	ft_putnbr(int n)
{
	if (n > -2147483648)
	{
		if (n < 0)
		{
			n = n * -1;
			ft_putchar('-');
		}
		if (n > 9)
		{
			ft_putnbr(n / 10);
			ft_putchar((n % 10) + '0');
		}
		else
			ft_putchar((n % 10) + '0');
	}
	else
		ft_putstr("-2147483648");
}
