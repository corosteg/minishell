#include "libft.h"

void	ft_putendl(char const *s)
{
	int a;

	if (s != 0)
	{
		a = 0;
		while (s[a])
		{
			ft_putchar(s[a]);
			a++;
		}
	}
	ft_putchar('\n');
}
